package org.staff.settings.colortheme;

import org.eclipse.swt.graphics.Color;

public final class MainDialogColorTheme {
	private static Color GreenColor;
	private static Color RedColor;
	private static Color GrayColor;
	private static Color WhiteColor;
	
	private static Color HoverGreenColor;
	private static Color HoverRedColor;
	private static Color HoverGrayColor;
	private static Color HoverWhiteColor;
	public static Color getGreenColor() {
		return GreenColor;
	}
	public static void setGreenColor(Color greenColor) {
		GreenColor = greenColor;
	}
	public static Color getHoverGreenColor() {
		return HoverGreenColor;
	}
	public static void setHoverGreenColor(Color hoverGreenColor) {
		HoverGreenColor = hoverGreenColor;
	}
	public static Color getRedColor() {
		return RedColor;
	}
	public static void setRedColor(Color redColor) {
		RedColor = redColor;
	}
	public static Color getHoverRedColor() {
		return HoverRedColor;
	}
	public static void setHoverRedColor(Color hoverRedColor) {
		HoverRedColor = hoverRedColor;
	}
	public static Color getGrayColor() {
		return GrayColor;
	}
	public static void setGrayColor(Color grayColor) {
		GrayColor = grayColor;
	}
	public static Color getHoverGrayColor() {
		return HoverGrayColor;
	}
	public static void setHoverGrayColor(Color hoverGrayColor) {
		HoverGrayColor = hoverGrayColor;
	}
	public static Color getWhiteColor() {
		return WhiteColor;
	}
	public static void setWhiteColor(Color whiteColor) {
		WhiteColor = whiteColor;
	}
	public static Color getHoverWhiteColor() {
		return HoverWhiteColor;
	}
	public static void setHoverWhiteColor(Color hoverWhiteColor) {
		HoverWhiteColor = hoverWhiteColor;
	}

}
